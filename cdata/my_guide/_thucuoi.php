    <h2>Sói tinh</h2>
    Sói tinh thuộc dòng họ sói làm việc cho nữ thần Vearis. Sói Tinh xuất hiện cùng với sự hình thành của Lục địa MU từ
    hàng ngàn năm trước, nó được huấn luyện bởi các vị Pháp sư lỗi lạc trong suốt hàng ngàn năm và giờ đây, Sói Tinh đã
    lĩnh hội được sức mạnh, trí thông minh của loài người và phép thuật của các vị Thần trên lục địa MU. Nó được giao
    nhiệm vụ bảo vệ các vùng đất của lục địa và nhận lệnh của nữ Thần Verias giúp đỡ các chiến binh chống lại thế lực
    của trùm Quỷ Kundun và hơn thế nữa, nó còn cho các chiến binh sử dụng toàn bộ sức mạnh của mình để chống lại cái ác
    đang ngày đêm diễn ra trên Lục địa MU hàng ngàn năm về trước. <br>
    <br>
    <div align="center"><img width="70%" src="/public/images/guides/Fenrir/Content_clip_image002.jpg"><br>
        (Fenrir)
    </div>
    <br>
    <br>
    Sau khi lục địa MU bị thế lực Kundun chiếm giữ, các vị Pháp sư đã quyết định cất giấu linh hồn Sói Tinh. Để đảm bảo
    dòng dõi Sói Tinh không bị rơi vào tay của Kundun, các vị Pháp sư đã sử dụng phép thuật của mình phân chia Linh hồn
    Sói tinh thành nhiều đồ vật bình thường và cất giấu chúng rải rác trên lục địa MU . <br>
