<strong>HỆ THỐNG KHẢM NGỌC</strong>
<h2>A. Giới thiệu công việc</h2>
- Giới thiệu các item mới được bổ sung. <br>
<br>
- Item mới được đưa vào như item có socket, hạt nguyên tố , khuôn, ngọc   nguyên tố. <br>
<br>
<h2>B. Giới thiệu hệ thống</h2>
<h3>1.Giới thiệu</h3>
- Bằng cách tách những option từ item trước đây, lắp đặt   vào socket để tạo ra item mới. <br>
<br>
- Lắp đặt option item có tên gọi là ngọc   nguyên tố để tạo ra item có option phụ.
<h3>2. Sơ đồ cơ bản</h3>
Theo hệ thống sơ đồ quá trình diễn ra theo thứ tự sau:   &quot;tách → gắn kết → lắp đặt → phá hủy&quot; <br>
