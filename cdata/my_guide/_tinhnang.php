<h2>E. Gắn kết</h2>
<br>
<br>
Giới thiệu <br>
<br>
* Kết hợp hạt nguyên tố và khuôn   tạo ra ngọc nguyên tố. Gọi đây là công đoạn gắn kết. <br>
<br>
- Hạt nguyên tố có   thể nhận được trong quá trình tách. <br>
<br>
- Khuôn có thể nhặt được lúc đi săn   quái vật. <br>
<br>
* Có thể lắp vào socket item bằng seed sphere vừa tạo ra. <br>
<br>
* Quá trình gắn kết có thể tiến hành thông qua Seed Master giống như quá   trình tách rời.
<h2>Khuôn</h2>
<h3>1) Định nghĩa</h3>
<br>
* Là Item có với dạng tinh thể chứa hạt nguyên tố. <br>
<br>
* Nếu không có chứa hạt nguyên tố bên trong thì không thể lắp vào soket   được.
<h3>2. Hình thức sở hữu </h3>
<br>
* Có thể nhặt được khi săn quái vật (không đạt   được khi tách ra hoặc liên kết) <br>
