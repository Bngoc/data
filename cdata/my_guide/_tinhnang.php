<h2>Ngọc nguyên tố:</h2>
<br>
<h3>1. Định nghĩa</h3>
<br>
* Kết hợp hai item hạt nguyên tố và khuôn sẽ cho ra   sản phẩm cuối cùng là item ngọc nguyên tố. <br>
<br>
* Trong khi kết hợp ngọc   nguyên tố sẽ nhận được option mang hạt nguyên tố và giá trị số bị nén lại. <br>
<br>
* ngọc nguyên tố nếu được lắp vào socket thì có thêm option ở socket   item.
<h3>2. Hình thức sở hữu </h3>
<br>
