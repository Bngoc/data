<h3>Marlon ở Lorencia, Devias, Noria – Người giao nhiệm vụ Thân phận Anh hùng   (Hero Status Quest - Marlon)</h3>
<p align="center"><img src="http://season2.mu-hoangkim.com/library/images/img4C.jpg"></p>
Cấp độ tối thiểu để các nhân vật làm nhiệm   vụ Thân phận anh hùng là đạt đến 220 lvl và đã thực hiện nhiệm vụ thăng cấp <br>
<br>
Marlon sẽ giao cho Pháp Sư và Thánh Nữ 1 yêu cầu, riêng Hiệp Sĩ là 2 yêu   cầu <br>
<br>
Yêu cầu thứ nhất là tìm Nhẫn danh dự và phí tổn là 2 triệu zen. Có   thể tìm thấy Nhẫn danh dự ở Sa mạc chết (Tarkan) <br>
<br>
Sau khi trao cho Marlon   Nhẫn danh dự, nhân vật mới kết thúc nhiệm vụ <br>
<br>
Marlon sẽ thưởng cho người   chơi ở cấp độ 220 một điểm nâng cấp <br>
<br>
Nhưng nếu người chơi hoàn tất nhiệm   vụ ở cấp độ 250, họ sẽ được nhận đến 30 điểm thưởng. <br>
<br>
Đối với Hiệp Sĩ,   Marlon sẽ yêu cầu thực hiện thêm 1 nhiệm vụ - đi tìm Viên ngọc cổ. Phí tổn cho   nhiệm vụ là 3 triệu zen. Viên ngọc cổ cũng có thể tìm thấy ở Sa mạc chết <br>
<br>
Sau khi trở về gặp Marlon cùng với Viên ngọc cổ, Hiệp Sĩ sẽ được thưởng   1 kỹ năng kép. <br>
