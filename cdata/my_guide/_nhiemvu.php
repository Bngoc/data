<h2>Nhiệm Vụ 3</h2>
<h3>Nhiệm vụ giai đoạn 1:</h3>
● Phải hoàn thành nhiệm vụ 1 và 2.<br>
● Cấp độ yêu   cầu từ 380 trở lên.<br>
● Zen yêu cầu 5.000.000<br>
● Nhận nhiệm vụ gặp NPC Debin   tại Cry Wolf (288,48)<br>
● Tiêu diệt Phượng Hoàng Lửa (Icarus), Dơi Lửa   (Tarkan), Quỷ Vương (Aida) để thu nhận 3 món item quest đem về giao cho NPC   Debin.<br>
● Món item yêu cầu : Sừng Quỷ Vương, Linh Hồn Dơi Lửa, Lông Phượng   Hoàng.<br>
● Sau khi trả nhiệm vụ bạn sẽ nhận được 20 điểm thưởng.<br>
