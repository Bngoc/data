<h2>Nhiệm vụ thăng cấp (Character Advancement)</h2>
Một khi đã đạt được cấp độ cần   thiết, bạn sẽ có cơ hội để thăng cấp. Lần thăng cấp này sẽ thay đổi danh hiệu   của bạn cũng như tạo cho bạn năng lực để sử dụng nhiều loại vũ khí mạnh. Đây   cũng là dịp để chứng tỏ khả năng của bạn cho lần thực hiện nhiệm vụ “Thân phận   anh hùng” trong tương lai. <br>
<br>
Cách thực hiện và các món đồ yêu cầu <br>
<h3>Nhiệm vụ thăng cấp từ Nữ tu Sevina</h3>
<ul>
  <li>Cấp độ tối thiểu dành cho Chiến Binh, Phù Thủy và Tiên Nữ để thực hiện nhiệm   vụ là 150 </li>
  <li>Người chơi phải đến gặp và nói chuyện với Sevina để nhận nhiệm vụ </li>
  <li>Nữ tu Sevina sống tại Devias. </li>
  <li>Nhiệm vụ được chia thành 2 phần. Nhân vật phải hoàn tất cả 2 phần mới được   thăng cấp </li>
  <li>Phần 1 yêu cầu nhân vật đi tìm Cuộn sách đế vương và phí tổn 1 triệu zen -   Có thể tìm thấy Cuộn sách đế vương bằng cách hạ gục quái vật ở Lost Tower tầng 1 </li>
  <li>Sau khi tìm thấy Cuộn sách đế vương, nhân vật phải quay về gặp Sevina để   trao nó cho cô ta và nhận 10 điểm thưởng cùng với phần 2 của nhiệm vụ </li>
  <li>Phần 2 của nhiệm vụ yêu cầu nhân vật tìm các món đồ tùy thuộc vào loài   (Chiến Binh – Thanh gươm gãy, Phù Thủy – Linh hồn phù thủy, Tiên Nữ – Nước mắt   tiên nữ). Có thể tìm thấy các món đồ này ở các tầng của Lost Tower. </li>
</ul>
