<h3>Nhiệm vụ giai đoạn 2:</h3>
Để biểu hiện sự hùng mạnh của bạn cho nên Debin   muốn kiểm tra năng lực bằng cách : giao cho nhiệm vụ tham nhập vào doanh trại   của Balgass. <br>
<br>
● Cấp độ yêu cầu 400.<br>
● Zen 7.000.000<br>
● Nói chuyện   với Debin ở Cry Wolf (228,48) để nhận nhiệm vụ.<br>
● Đi đến bản đồ Cry Wolf tọa   độ (62, 239) để vào trong.<br>
● Giết 10 con mỗi loại : Balram, Death Spiritt,   Soram.<br>
● Sau khi hoàn thành về trả nhiệm vụ sẽ được 20 điểm thưởng.<br>
<br>
<h3>Nhiệm vụ giai đoạn 3:</h3>
