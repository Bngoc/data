<h3>Tạo Áo choàng cho Chúa Tể</h3>
<br>
<em>**Chú ý: Áo choàng sẽ rơi nếu bạn đang ở trong chế độ PvP. </em>
<ol>
    <li>Nói chuyện với Chaos Goblin (Yêu tinh sáng tạo) tại Noria (vị trí 180,100).</li>
    <li>Chọn mục &quot;Regular Combination&quot; (Kết hợp thông thường)</li>
    <li>Khi các ô chứa nguyên liệu xuất hiện (có 8x4 ô), bỏ vào:
        <ul>
            <li>1 Huy hiệu hoàng tộc (Crest of Monarch)</li>
            <li>1 viên Ngọc Chaos.</li>
            <li>1 cánh cấp 1 (First Stage Wing)</li>
            <li>Chi phí sẽ được tự động tính toán cho lần ghép đó, nếu đồng ý, bạn chọn nút &quot;Combine&quot; (Kết
                hợp)
            </li>
        </ul>
    </li>
    <li>Nếu quá trình ghép thành công, áo choàng sẽ được tạo ra và các nguyên liệu sẽ biết mất.</li>
    <li>Nếu quá trình ghép bất thành, tất cả chi phí và nguyên liệu sẽ biến mất.</li>
</ol>