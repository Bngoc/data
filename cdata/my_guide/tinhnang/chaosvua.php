Công thành là một trong những chức năng thú vị và hoành tráng nhất   của MU Hoàng Kim. Với số lượng bang hội hùng mạnh không ngừng gia tăng   và nhiệt huyết của vô số chiến binh trên lục địa, những trận công   thành nảy lửa đầy kịch tính đã diễn ra hàng tuần, hàng tháng đem   lại cho MU một không khí rất sôi động và cuốn hút. Công thành để thỏa   mãn sức mạnh của cá nhân, của cộng đồng và cũng để tìm kiếm danh   vọng, quyền lực, phú quý, giàu sang.
<br>
<br>
Tính năng kết hợp chaos   của Vua là sự khẳng định vị thế của người chiến thắng, sự khẳng   định quyền lực và sự sở hữu hoàn toàn đặc quyền dành cho người   xứng đáng với nó.
<br>
<br>
Tính năng kết hợp chaos giúp nhà Vua có thể   tạo ra những báu vật quý hiếm trên thế gian. Ngoài việc đem lại của   cải giàu sang từ nó, những báu vật này sẽ đem lại cho nhà Vua sức   mạnh cùng quyền lực để thống trị và hợp nhất lục địa MU.
<br>
<br>
Để   có thể sử dụng tính năng Kết hợp chaos của Vua, Vua phải vào thành   và chọn sử dụng Máy chaos của Hoàng gia.
<br>
