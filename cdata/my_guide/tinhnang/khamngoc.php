<strong>HỆ THỐNG KHẢM NGỌC</strong>
<h2>A. Giới thiệu công việc</h2>
- Giới thiệu các item mới được bổ sung. <br>
<br>
- Item mới được đưa vào như item có socket, hạt nguyên tố, khuôn, ngọc nguyên tố. <br>
<br>
<h2>B. Socket Items</h2>
<em><strong>Giới thiệu</strong></em> <br>
<br>
* Có thể nhặt được chiến phẩm rơi ra trong lúc săn quái vật, option của nó giống như item thông thường. <br>
<br>
- Sức tấn công bổ sung, sức chống đỡ, ma lực, tỉ lệ phòng ngự, may mắn, sức sống tự động phục hồi. <br>
<br>
* Số socket <br>
<br>
- Số socket trong một item có từ 1 đến 3 socket ( vũ khí, cung có thể giữ tối đa là 5) <br>
<br>
- Số socket thiết lập từ khi món vũ khí rơi ra và không thể tăng hoặc giảm <br>
<br>
* Item có socket đều có những đặc tính giống item bình thường, có thể nâng cấp lên +13và giao dịch bình thường. <br>
<br>
<h2>C. Tách rời</h2>
<h3>1. Giới thiệu</h3>
<br>
* Với nguyên liệu là item hoàn hảo và item set sẽ tạo thành item mới tên gọi là Seed có option đặc trưng như: Lửa, Nước, Sét, Đất, Băng, Gió.
<br>
<br>
* Hạt nguyên tố được tạo thành sẽ kết hợp với khuôn tạo thành ngọc nguyên tố. <br>
<br>
