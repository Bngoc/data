<h3>Chế tạo Quái Điểu (Dinorant)</h3>
<br>
<div>
    <div align="center"><img height="200" src="/images/guides/mountsPets.png" width="70%"></div>
</div>
<br>
<br>
1.Nói chuyện với Chaos Goblin (Yêu tinh sáng tạo)tại Noria (vị trí 180,100). <br>
<br>
2. Chọn mục &quot;Kết Hợp Bình Thường&quot; <br>
<br>
3. Khi các ô chứa nguyên liệu xuất hiện (có 8x4 ô), bỏ vào: <br>
• 10 con Kỳ Lân (Horn of Unicorn) - (tất cả Kỳ Lân phải còn nguyên lượng máu (HP)) <br>
• 1 viên Ngọc Chaos. <br>
<br>
4. Chi phí sẽ được tự động tính toán cho lần ghép đó. <br>
<br>
5. Sau khi đồng ý với tỉ lệ thành công của lần ghép này, bạn chọn nút &quot;Combine&quot; (Kết hợp) <br>
<br>
6. Nếu quá trình ghép thành công, Quái Điểu (với các tính năng ngẫu nhiên) sẽ xuất hiện. <br>
<br>
7. Nếu quá trình ghép bất thành, tất cả chi phí sẽ biến mất và nguyên liệu sẽ biến mất.