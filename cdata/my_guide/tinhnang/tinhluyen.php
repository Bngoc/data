<h2>A. Cách tạo hòn đá của tạo hoá để gia cường Items:</h2>
- Bạn cần phải thu thập Đá Nguyên Thủy trong vương quốc ngầm Kanturu. <br>
<br>
- Sau đó đến Vùng đất Kanturu_Remain ở tọa độ 140-188 để bước vào Tháp Tinh Luyện. (Cần phải chiến thắng được Nightmare)
<br>
<br>
- Tìm gặp NPC Elpis (78 - 174) chuyên tinh chế Đá Nguyên Thủy <br>
<br>
<div>
    <div align="center"><img height="350" src=" /images/guides/npc-Elpis.jpg" width="80%"></div>
</div>
<br>
- Chọn chức năng Tinh luyện Đá Nguyên Thủy <br>
<br>
- Đặt 1 viên Đá Nguyên Thủy vào máy tinh chế, sau đó chọn Tinh chế. Nếu thành công bạn sẽ nhận được Hòn Đá của Tạo Hóa
<br>
- Tỉ lệ thành công là: 50% <br>
<br>

<h2>B. Cách Tạo Đá Tinh Luyện: (Nâng cấp độ tính năng gia cường)</h2>
- Để tạo được Đá Tinh Luyện bạn cần phải có Item thường hoặc hoàn hảo. <br>
<br>
- Người chơi gặp <strong>NPC Osbourne</strong> chuyên chế tạo các loại Đá Tinh Luyện ở Aida tọa độ 80-12. <br>
<br>
<div>
    <div align="center"><img height="350" src=" /images/guides/npc-Osbourne.jpg" width="80%">
    </div>
</div>
<br>
<br>
- Đá Tinh Luyện có 2 dạng là Đá Tinh Luyện Cấp Thấp và Đá Tinh Luyện Cấp Cao. <br>
<br>
<h2>C. Phục hồi:</h2>
<h3>1. Mục đích</h3>
<br>
- Vì item gia cường sẽ không thể giao dịch được nên chức năng này sẽ giúp cho người chơi phục hồi item gia cường trở về trạng thái ban đầu
<br>
<br>
- Ngoài ra, khi người chơi không hài lòng với tính năng gia cường, họ có thể phục hồi để gia cường tính năng khác mà mình mong muốn.
<br>
<br>
<h3>2. Hướng dẫn:</h3>
<br>
- Người chơi gặp NPC Cheris chuyên Phục hồi các item gia cường tại Aida tọa độ 86-12. <br>
<br>
<div>
    <div align="center"><img height="400" src="/public/images/guides/npc-Cheris.jpg" width="80%">
    </div>
</div>
<br>
- Khi quá trình Phục hồi thành công, tính năng gia cường sẽ biến mất và dòng chữ “không thể giao dịch” cũng sẽ mất theo đó.
<br>
<br>
- Tỉ lệ thành công của quá trình Phục hồi là 100%. <br>
<br>
