<h3>Các bước thực hiện chế tạo các món đồ Chaos</h3>
<br>
1) Nói chuyện với Chaos Goblin (Yêu tinh sáng tạo) tại Noria (vị trí 180,100).<br>
<br>
2) Chọn mục “Chaos Weapon Combination” (Tạo vũ khí chaos)<br>
<br>
3) Khi các ô chứa nguyên liệu xuất hiện (có 8x4 ô), bỏ vào: •  Nguyên liệu có cấp độ 4 (tối thiểu) và có tính năng +4 (tối thiểu) •  Một hoặc nhiều viên Ngọc Chaos.
<br>
<br>
4) Chi phí sẽ được tự động tính toán cho lần ghép đó.<br>
<br>
5) Sau khi đồng ý với tỉ lệ thành công của lần ghép này, bạn chọn nút “Combine” (Kết hợp)<br>
<br>
6) Nếu quá trình ghép thành công, một cây Rìu Rồng hoặc Cung Siêu Nhiên hoặc Gậy Thần Sét sẽ xuất hiện ngẫu nhiên và các nguyên liệu sẽ tự động biến mất.
<br>
<br>
7) Nếu quá trình ghép bất thành, tất cả chi phí và Ngọc Chaos sẽ biến mất, các món nguyên liệu sẽ bị giảm cấp độ cũng như tính năng.