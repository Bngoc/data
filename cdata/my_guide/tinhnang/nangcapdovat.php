<h2>Nâng cấp Items lên +9</h2>
<h3>Để nâng cấp được item lên cấp độ cao, trước tiên bạn phải nâng cấp lên +9</h3>
Nâng cấp item từ 0 đến 6 các bạn sử dụng ngọc ước nguyện hoặc tâm linh.   Sử dụng ngọc ước nguyện tỷ lệ 100% thành công, đối với ngọc tâm linh chỉ được   50% thành công. Để nâng cấp item từ 6 đến 9 bắt buộc phải dùng ngọc tâm linh. Tỷ   lệ nâng cấp sẽ được +25% thành công đối với những món item có &quot;Luck&quot;.
<br><br>
<h2>Nâng cấp Items lên +10</h2>
<h3>Các bước thực hiện nâng cấp món đồ lên +10</h3>
<ol>
    <li>Nói chuyện với Chaos Goblin (Yêu tinh sáng tạo)tại Noria (vị trí 180,100).</li>
    <li>Chọn mục &quot;Kết Hợp Bình Thường&quot;</li>
    <li>Khi các ô chứa nguyên liệu xuất hiện (có 8x4 ô), bỏ vào:
        <ul>
            <li>Món đồ +9 cần nâng cấp</li>
            <li>1 viên Ngọc Chaos.</li>
            <li>1 viên Ngọc Ước Nguyện - để nâng cấp lên +10</li>
            <li>1 viên Ngọc Tâm Linh - để nâng cấp lên +10</li>
            <li>Chi phí sẽ được tự động tính toán cho lần ghép đó, nếu đồng ý, bạn chọn nút &quot;Kết hợp&quot;.</li>
        </ul>
    </li>
    <li>Nếu quá trình ghép thành công, món đồ cần nâng cấp sẽ tăng cấp độ lên +10</li>
    <li>Nếu quá trình ghép bất thành, tất cả chi phí và nguyên liệu sẽ biến mất.</li>
</ol>
