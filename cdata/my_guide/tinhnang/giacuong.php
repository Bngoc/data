<h3>I. Khái niệm về gia cường Items:</h3>
<br>
- Cũng như cách bạn tăng cấp độ cho   item của mình bằng Ngọc Ước Nguyện, bạn có thể sử dụng phương pháp gia cường này   khi Hòn Đá Tạo Hóa.
<br>
<br>
- Khi gia cường thành công item của bạn sẽ có thêm 1   số tính năng mới ngẫu nhiên <br>
<br>
- Để tăng cấp độ cho tính năng mới gia cường   bạn có thể dùng Đá Tinh Luyện cấp thấp hoặc cao. (Tỉ lệ thành công khi sử dụng   Đá Tinh Luyện Cấp thấp là 2% và 30% khi sử dụng loại Đá Tinh Luyện Cấp Cao)
<br>
<br>
- Cấp độ của tính năng gia cường tỉ lệ thuận với cấp độ của item. <br>
<br>
- Item thông thường hoặc hoàn hảo đều có thể gia cường <br>
<br>
- Tỉ lệ   thành công khi sử dụng Hòn Đá Tạo Hóa là 5%. <br>
<br>
- Trong trường hợp bạn   không ưng ý với tính năng mới đã được gia cường, bạn có thể Phục hồi item về   trạng thái ban đầu và tiếp tục quá trình gia cường tính năng mới.
<br>
<br>
<h3>II. Lưu ý:</h3>
- Item đã được gia cường sẽ không thể giao dịch, mua bán   (NPC, cửa hàng cá nhân), hoặc quăng ra. <br>
<br>
- Nếu sử dụng chức năng Phục   hồi thì tất cả các tính năng gia cường đã có sẽ biến mất và nó trở về trạng thái   bình thường
<br>
