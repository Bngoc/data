<h3>Thương nhân Servis</h3>
<br>
Servis là một trong những học trò của những Đại   pháp sư tài ba nhưng ông đặc biệt có hứng thú đối với thuật giả kim và các phép   thuật dùng để sáng tạo hay kết hợp các loại khoáng sản, ngọc quý hơn là nghiên   cứu các phép thuật chiến đấu thật sự. Mục đích lớn nhất trong đời Servis là ông   có thể tạo ra một hòn đá ma thuật với sức mạnh vô tận tương tự như Hòn đá phong   ấn mà pháp sư Etramus dùng để phong ấn Kundun. Thế nhưng trong lúc nghiên cứu   cách chế tạo hòn đá ma thuật, ông đã vô tình phát minh ra được phương pháp mới.   Phương pháp này có thể cô đặc sức mạnh của nhiều viên ngọc lại thành một khối,   tạo nên sự thuận tiện khi cất giữ, bảo quản và giao dịch hàng chục viên ngọc 1   lúc.
<br>
<br>
<h3>Cách nén ngọc</h3>
<br>
Tìm NPC Servis nén ngọc thì có thể nén ngọc Ước Nguyện   hay ngọc Tâm Linh thành một cụm 10 viên, 20 viên, 30 viên và phải trả chi phí   nén (Zen).
<br>
<br>
Ngọc được nén với đơn vị là 10 viên thì không thể sử dụng để   nâng cấp item và có thể giải nén mỗi viên bằng NPC. <br>
<br>
NPC Servis nén ngọc   đang ở tọa độ: 36 , 241 trong làng Elbeland. <br>
<br>
Click vào NPC Servis, có   ngọc cần nén ở thùng đồ cá nhân, nếu cửa sổ menu nén mở ra thì bấm chọn vào nén   ngọc. <br>
<br>
