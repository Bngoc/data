<h3>&quot;Hệ thống nén ngọc&quot; để giao dịch item thuận tiện trong game</h3>
<br>
Trước   đây, khi cất giữ một số lượng lớn ngọc trong nhà kho hay trong Thùng đồ cá nhân,   chúng ta chỉ có thể cất giữ riêng lẻ từng viên một, và khi giao dịch ngọc với   một số lượng lớn, điều này gây ra sự vô cùng bất tiện đối với người chơi. Do   vậy, để giảm thiểu tối đa sự bất tiện này, người ta đã thêm vào hệ thống kết hợp   ngọc để có thể cất giữ vào 1 ngăn của Thùng đồ cá nhân/ thùng đồ 1 lúc 10, 20   hoặc 30 viên ngọc.
<br>
<br>
Chỉ có Ngọc Ước nguyện và Ngọc Tâm Linh mới có thể   kết hợp. <br>
