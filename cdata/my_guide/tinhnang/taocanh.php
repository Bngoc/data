<h3>Chế tạo Cánh cấp 1</h3>
<ol>
    <li>Nói chuyện với Chaos Goblin (Yêu tinh sáng tạo. tại Noria (vị trí 180,100)</li>
    <li>Chọn mục &quot;Kết hợp Bình Thường&quot;</li>
    <li>Khi các ô chứa nguyên liệu xuất hiện (có 8x4 ô., bỏ vào:
        <ul>
            <li>Vũ khí Chaos có cấp độ 4 (tối thiểu) và có tính năng +4 (tối thiểu)</li>
            <li>Một hoặc nhiều viên Ngọc Chaos.</li>
        </ul>
    </li>
    <li>Chi phí sẽ được tự động tính toán cho lần ghép đó.</li>
    <li>Sau khi đồng ý với tỉ lệ thành công của lần ghép này, bạn chọn nút &quot;Combine&quot; (Kết hợp)</li>
    <li>Nếu quá trình ghép thành công, một Cánh thiên đàng hoặc Cánh Satan hoặc Cánh tiên nữ hoặc Cánh Summoner sẽ xuất
        hiện ngẫu nhiên. Các nguyên liệu sẽ tự động biến mất.
    </li>
    <li>Nếu quá trình ghép bất thành, tất cả chi phí và Ngọc Chaos sẽ biến mất, các món nguyên liệu sẽ bị giảm cấp độ
        cũng như tính năng.
    </li>
</ol>
<h3>Chế tạo Cánh cấp 2</h3>
<ol>
    <li>Nguyên liệu sử dụng bao gồm:
        <ul>
            <li>Một hoặc nhiều viên Ngọc Chaos.</li>
            <li>Cánh cấp 1 cấp độ từ +4 và tính năng +4 trở lên</li>
            <li>Lông vũ</li>
        </ul>
    </li>
    <li>Nếu đồng ý với tỉ lệ thành công , bạn chọn nút &quot;Combine&quot;</li>
    <li>Nếu thành công sẽ tạo được Cánh cấp 2.</li>
    <li>Hoặc thất bại tất cả nguyên liệu bị giảm cấp độ và tính năng, còn ngọc sẽ biến mất.</li>
</ol>