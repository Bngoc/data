<ul>
    <li>Khi HP thú nuôi xuống tới 0 trong lúc chúng đang được sử dụng, thì chúng sẽ không thể tấn công và mất đi 10%
        điểm kinh nghiệm.
    </li>
    <li>Quái vật cấp độ 102 có thể rơi ra Linh hồn Chiến mã.</li>
    <li>Quái vật cấp độ 96 có thể rơi ra Linh hồn Quạ tinh.</li>
</ul>
<h3>Cách kết hợp nguyên liệu tạo Quạ Tinh:</h3>
<div>
    <div align="center"><img height="171" src="/images/guides/quatinh.jpg" width="159"></div>
</div>
<ol>
    <li>Nói chuyện với Nhà huấn luyện thú ở toạ độ (122,110) ở Lorencia.</li>
    <li>Chọn chức năng Resurrect Spirit.</li>
    <li>Trong cửa sổ ghép đồ, đặt vào:
        <ul>
            <li>Một (1) Linh hồn Chiến mã</li>
            <li>Năm (5) Ngọc Bless</li>
            <li>Năm (5) Ngọc Soul</li>
            <li>Một (1) Ngọc Creation</li>
            <li>Một (1) Ngọc Chaos</li>
            <li>5 triệu Zen</li>
        </ul>
    </li>
    <li>Sau khi thấy và chấp nhận tỷ lệ thành công, nhấn nút &quot;Combine&quot;</li>
    <li>Nếu kết hợp thành công, 1 con Chiến mã sẽ được hồi sinh.</li>
    <li>Trong trường hợp kết hợp thất bại, tất cả nguyên liệu và Zen sử dụng cho việc tạo thú nuôi sẽ biến mất.</li>
</ol>
