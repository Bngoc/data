<p>Một số người làm chủ quyền năng của phép thuật ở độ tuổi rất nhỏ, một số khác làm chủ quyền năng của các thanh gươm
    và một số ít khác họ có thể lĩnh hội được cả hai quyền năng đó. Họ có thể sử dụng thành thạo cả gươm và gậy phép. Họ
    là những nhà nghiên cứu về hai trường phái sức mạnh và phép thuật. Mọi người gọi họ là Đấu Sĩ. Đấu Sĩ đã tìm ra bí
    mật của cách thi triển phép thuật khi đang mang trên người các bộ giáp trụ mà luôn gây nhiều trở ngại cho các pháp
    sư. Họ cũng có thể sử dụng gươm điêu luyện như các hiệp sĩ. Đấu Sĩ có thể học và phát triển nhanh hơn các dòng tộc
    khác.</p>
<p>Đấu Sĩ có thể sử dụng một loạt các phép thuật, kỹ năng và áo giáp mà Chiến Binh và Phù Thủy thường dùng. Mặc dù các
    kỹ năng chiến đấu và phép thuật không hoàn hảo như Chiến Binh hay Phù Thủy thượng đẳng, nhưng sự kết hợp này làm anh
    ta thật sự là một nhân vật đột phá nhất trong các dòng nhân vật. Đấu Sĩ có khả năng thi triển các đòn đánh quyết
    định, họ có thể sử dụng các món đồ mạnh hay đi vào những vùng đất khác nhau với yêu cầu cấp độ thấp hơn các dòng
    nhân vật khác. Cuối cùng để có được Đấu Sĩ bạn phải có một nhân vật cơ bản đạt cấp độ 220 (hai trăm hai mươi). </p>
