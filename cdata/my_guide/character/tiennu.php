<p>Tiên nữ của vùng Noria là nhân vật thuộc những dòng tộc cổ xưa. Họ cho rằng họ là dòng tộc đầu tiên của MU trước cả
    con người và yêu tinh. Một Tiên nữ ở Noria đã truyền lại rằng họ có một vẽ đẹp huyền ảo và duyên dáng. Tuy họ có vẻ
    tránh né những trận chiến nhưng không thể nói rằng họ nhút nhát. Các Tiên nữ đã dạy cho con người những kỹ năng dùng
    cung điệu nghệ. Không ai có thể chiến thắng Tiên nữ trong một trận chiến bằng cung. Những Tiên nữ của Noria cũng có
    những giao ước với các dòng tộc khác và những hiệp ước đó đã cuốn họ vào các cuộc chiến. </p>
<p>Những khả năng của Tiên nữ là một phần không thể thiếu trong một nhóm. Năng lượng của Tiên nữ có thể hỗ trợ phép
    thuật như tăng sức chống đỡ, tăng sức mạnh hay phục hồi sức khỏe và họ có thể triệu hồi các quái thú trong các trận
    chiến. Sức mạnh Tiên nữ làm sửng sốt các chiến binh tầm xa, họ tấn công rất nhanh và khả năng tránh đòn cao là các
    Tiên nữ dòng này trở nên những tay đánh “giáp lá cà” tuyệt vời.<br>
    <br>
</p>
