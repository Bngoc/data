<p align="center"><img src="/public/images/web/guides/img_summoner.jpg"> <br>
    Sự xuất hiện của phiên bản Season IV hứa hẹn sẽ đem đến nhiều điều bất ngờ và thú vị cho người chơi MU qua những bản
    đồ mới, boss mới, items mới và những bí mật đang chờ được khám phá. Tuy nhiên, điều nổi bật nhất và đang thu hút sự
    chú ý của toàn thể các chiến binh trên lục địa đó chính là sự ra mắt của một nhân vật (character) mới bên cạnh 5
    nhân vật quen thuộc từ lâu: Summoner. <br>
    <br>
    Summoner là dòng nhân vật gì ? Sử dụng loại phép thuật hay vũ khí nào ? Và sẽ đóng vai trò ra sao trong cuộc chinh
    chiến chống lại cái ác trên MU ? Để giúp các bạn có một cái nhìn tổng quát và hiểu biết nhất định về Summoner, chúng
    tôi xin giới thiệt vài nét sơ lược về nhân vật mới đầy hấp dẫn này. <br>
    <br>
    Summoner được ra đời từ Elbeland, ngôi làng của người ẩn dật nằm ở phía Tây Nam của lục địa MU. Theo truyền thuyết
    lâu đời, đây là nơi sinh sống của những người có khả năng siêu nhiên, họ rất nhạy cảm và dễ dàng giao tiếp được với
    các linh hồn chưa siêu thoát. Do đặc điểm nguồn gốc đó, Summoner được thừa hưởng tất cả những phép thuật của tổ
    tiên.Họ lấy phép thuật làm vũ khí chính. Ngoài ra họ có kỹ năng hút máu và gọi linh hồn để hỗ trợ cho bản thân. Đối
    thủ khi bị linh hồn đánh trúng sẽ bị ếm bùa và mất một lượng máu khá lớn. <br>
    <br>
    Mọi người có thể nhận diện Summoner qua vẻ bề ngoài quyến rũ đầy mê hoặc, sự lạnh lùng bí ẩn và thanh gậy phép nhọn
    như giáo luôn cầm trên tay.</p>
