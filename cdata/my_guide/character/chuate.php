<p>Chúa Tể có vẻ bề ngoài như nhân vật phục vụ cho KunDun. Họ là dòng dõi của các nhà lãnh đạo, người đã chỉ huy những
    đội quân hùng hậu và thừa hưởng quyền năng của tạo hóa trong thời hoàng kim. Lời đe dọa của KunDun đã mang họ trở về
    giúp con người sống sót qua thời kỳ khó khăn nhưng một số khác trở về với mục đích chờ thời cơ để nắm lấy quyền lực
    khi cuộc chiến đang ngày càng gần kề.</p>
<p>Chúa Tể vung gươm ra hiệu với uy lực của một vị lãnh tụ trong các trận chiến. Họ có sức mạnh triệu hồi quạ tinh và
    chiến mã giúp họ đàn áp kẻ thù của mình. Như Đấu Sĩ, họ có thể xâm nhập vào những vùng đất và mang trên người những
    món đồ tốt với yêu cầu thấp hơn các dòng nhân vật khác. Bên cạnh đó, họ có những phép thuật mới và kỹ thuật chưa
    từng thấy trên lục địa MU. Cuối cùng, để có được Chúa Tể bạn phải có một nhân vật cơ bản đạt cấp độ 250 (hai trăm
    năm mươi).</p>
