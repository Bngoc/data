<div><br>
  Hội đóng vai trò rất   quan trọng trong Game MU.</div>
<div><br>
  Hội giúp tạo ra mối   liên hệ chặt chẽ giữa những nhân vật, giúp cho người chơi có cảm giác an toàn   hơn khi đang phiêu lưu trong vùng đất MU. </div>
<div><strong><br>
  Cách tạo Bang - Hội </strong></div>
<div><br>
  Khi bạn muốn tạo   Hội, bạn chỉ việc đến gặp người quản lý Hội tại trung tâm Devias (215,45) và   đăng ký tạo Hội. Tuy nhiên, bạn cần phải thỏa mãn một số điều kiện để có thể   thành lập Hội: </div>
<p><br>
  • Chủ Hội phải có cấp độ 100 trở   lên.</p>
<p>• Tên Hội phải từ 4 kí tự trở lên </p>
