<div>Câu lệnh: /battlesoccer   (Tên hội)</div>
<div> </div>
<div>Cách khác để giải quyết   mâu thuẫn xảy ra trong MU là tổ chức trận cầu giữa các Hội. Trong trận cầu này,   mỗi Hội đề cử 1-5 thành viên tạo thành một nhóm, hai nhóm sẽ gặp nhau tại Arena   - địa điểm chuyên dành cho các trận tranh cầu. Tiến hành thi đấu bằng cách ghi   điểm và hạ gục đối phương.</div>
<div><br>
  Điểm số được tính   khi hạ gục được thành viên (chỉ cho phép trên máy chủ hỗ trợ giao đấu) hoặc đánh   banh vào cầu môn của nhóm kia.</div>
<p>•  Mỗi bàn thắng được tính 20   điểm.</p>
<p>•  Tiêu diệt 1 nhân vật được 1 điểm (chỉ áp dụng trên   máy chủ hỗ trợ giao đấu).</p>
<p>•  Nhóm thắng là nhóm đầu tiên đạt 100   điểm</p>
