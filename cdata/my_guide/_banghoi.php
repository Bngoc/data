<div><br>
    <strong>Biểu tượng   Hội (Guild Mark)</strong></div>
<div><br>
  Sau khi thông báo   cho người quản lý Hội dự định lập Hội của mình, bạn phải cung cấp tên Hội và tạo   biểu tượng Hội. Sau khi tên Hội và biểu tượng Hội được tạo xong, bạn không được   quyền chỉnh sửa. Do đó, chúng tôi khuyên bạn nên suy nghĩ và lựa chọn chính xác   khi đặt tên và tạo biểu tượng cho Hội. </div>
<div><br>
    <strong>Hướng dẫn   nhanh về Hội </strong></div>
<p><br>
  •  Số lượng thành viên tối đa của Hội bằng 10%   cấp độ của chủ Hội.</p>
<p>   Ví dụ: Chủ Hội có cấp độ 350 thì số thành viên tối   đa của Hội là 35 (10% của 350).</p>
<p>•  Phím tắt của trình đơn Hội là   “G”.</p>
<p>•  Mời ai đó vào Hội bạn nhập “/guild” và trỏ chuột   vào nhân vật muốn mời.</p>
<p>•  Đưa ra lời thách đấu với Hội khác bạn nhập “/war   (Tên Hội)”.</p>
<p>•  Đưa ra yêu cầu tạo trận cầu với Hội khác bạn nhập   “/battlesoccer(Tên Hội)”.</p>
<p>•  Để gia nhập Hội bạn nhập “/guild” và trỏ chuột vào   chủ Hội.</p>
<p>•  Chủ Hội có thể thông báo cho các thành viên trong   Hội bằng cách nhập @&gt; trước thông báo.</p>
<div><br>
    <strong>Hủy bỏ hoặc   rời khỏi Hội</strong></div>
<div> </div>
<div><img height="175" alt="" src="http://mu.gate.vn/images/Editor/image/website/Bang%20hoi/image005.gif" width="217" onload="ResizeImages(this) ">          <img height="175" alt="" src="http://mu.gate.vn/images/Editor/image/website/Bang%20hoi/image006.gif" width="217" onload="ResizeImages(this) "></div>
<div>        <br>
  Vì lý do nào   đó mà bạn muốn hủy bỏ Hội của mình, nhấn “G” và sau đó chọn “Cancel”. Hội sau   khi đã bị hủy sẽ không thể phục hồi lại. Do đó, chúng tôi khuyên bạn nên cẩn   thận trước khi thực hiện việc này.<br>
  Nếu như bạn đang là thành viên của Hội,   không phải là chủ Hội, và bạn muốn thoát khỏi Hội. Bạn nhấn “G” sau đó nhấn nút   “Leave” để rời khỏi Hội. Người rời khỏi Hội có thể gia nhập lại nếu như hội vẫn   còn tồn tại. </div>
<div><strong><br>
  Thách đấu giữa các   Hội (Guild Duel)</strong></div>
<div> </div>
<div>Câu lệnh: /war (Tên   Hội)</div>
<div> </div>
<div>Chủ Hội có quyền đưa ra   lời thách đấu với một Hội khác. Khi Hội khác nhận được và đồng ý lời thách đấu,   ngay lập tức thành viên của 2 Hội có thể đấu với   nhau.</div>
<div><br>
  Trong trận đấu giữa   các Hội, không có món đồ nào rớt ra và không thành viên nào của 2 Hội được điểm   kinh nghiệm.  </div>
<div><br>
  Khi cuộc thách đấu   giữa Hội của bạn và Hội khác diễn ra, màn hình hỏi bạn có tham gia vào cuộc   thách đầu hay không sẽ hiện ra. Bạn có thể từ chối bằng việc nhấn vào nút   “Cancel”. Trong suốt cuộc phiêu lưu trên vùng đất MU, đôi khi bạn không muốn bị   những lời mời như thế quấy rầy. Bạn có thể khoá tất cả các lời mời trao đổi, gia   nhập nhóm, hoặc gia nhập cuộc thách đấu gởi đến bằng cách nhập “/request off”.   Nếu bạn muốn nhận được lời mời tham gia vào cuộc thách đấu bạn nhập “/request   on” để cho phép các lời mời gởi đến bạn.  </div>
