    tạo ra Elpis và Nightmare. Còn có người cho rằng Ngọc Gia Cường là một trong số những nguyên liệu được Estramu dùng
    tạo nên chiếc hộp đá để phong ấn Kundun. Giờ đây khi Kundun đã được hồi sinh, cả thế giới lại tập trung sự chú ý vào
    khả năng của Ngọc Gia Cường. <br>
    <br>
    <h3>4. Sinh vật lai tạo:</h3>
    <br>
    Đây là những vật thể sống được tạo ra bằng cách sắp xếp những ưu điểm của nhiều sinh vật khác nhau. Những sinh thể
    này là kết quả của ước nguyện tạo ra giống lai hoàn hảo mới có thể thích nghi với môi trường khắc nghiệt dễ dàng của
    người dân Kanturu. <br>
    <br>
    Tuy nhiên, thay vì tạo ra những cá thể tiến bộ như Elpis và Nightmare, họ lại hy sinh hàng ngàn những sinh thể lai
    và quẳng đi những “con lai không đạt chuẩn” có hình thù kì quái và kém chức năng vào hoang mạc Tarkan, từ đó hệ
    động thực vật tại đây dần bị phá hủy và tuyệt chủng.
