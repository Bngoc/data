    những hình ảnh tuyệt đẹp, cực kỳ sinh động và những kỹ thuật tiên tiến của Open GL Full 3D Engine, WebZen đã biến
    những điều không thể thành có thể. Điều gì nằm sau các truyền thuyết và huyền thoại cố xưa đầy bí ẩn của lục địa MU?
