  Nằm ở huớng Nam làng Lorencia tại tọa độ (220;15) là lối vào kinh thành   Lorencia. Nằm ở hướng Đông Nam kinh thành là lối vào Pháo đài sói   Crywolf.</div>
<div>Pháo đài Crywolf có tới 3   lối vào ở các tọa độ : (153-32) (120-62) (89-32). Trong pháo đài có sự xuất hiện   của một số NPC như: Thợ rèn Hanzo(145-13);Người quản lí công thành (119-11); Lái   buôn Lahap (97-25): Kẻ bán dạo Feddier(100-40); Người giữ đồ Bas(113-55); Nhà   huấn luyện thú (136-47).<br>
  Ngày xảy ra trận đánh, các NPC này sẽ ra đi để tránh   thương vong . Vì thế rất khó khăn nếu các chiến binh không chuẩn bị đầy đủ lương   thực và khí giới.</div>
