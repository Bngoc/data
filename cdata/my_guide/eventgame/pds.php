<div>Crywolf là cửa ngỏ đầu tiên   cản bước đoàn quân ma quỷ của KUNDUN. Muốn tiến được về phương bắc thì nhất   thiết Ma Vương KUNDUN phải chiếm được CryWolf. Cũng như vậy để cản bước KUNDUN   tất cả cư dân các bộ tộc tụ họp về đây, kết hợp với sức mạnh của Thần Sói để bảo   vệ cửa ngõ kinh thành Lorencia. </div>
<div>Do có tới 3 lối dẫn vào   pháo đài Crywolf nhưng lại không có cổng thành nên những trận chiến ở Crywolf   luôn là những trận chiến khốc liệt, không khoan nhượng. Cứ theo chu kỳ mỗi tháng   một lần Tượng thần Sói sẽ suy yếu không còn khả năng bảo vệ Pháo đài Crywolf.   Lúc đó binh đoàn Balgrass lại được lệnh tổng tấn công vào pháo đài này. <br>
